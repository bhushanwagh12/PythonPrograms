
# Using builtin Functions on a tuple
t1 = (10,20,30,40,50)

# finding the length of tuple
length = len(t1)
print(length)

# finding max element of tuple
max_value = max(t1)
print(max_value)

# finding min element of tuple
min_value = min(t1)
print(min_value)

# finding sum of a tuple
print(sum(t1))

print("--------------------------")

# type conversion functions
t1 = (10,"Sathya",False,10)
print(t1)
print(type(t1))

# converting tuple to list
l1 = list(t1)
print(l1)
print(type(l1))

# converting tuple to set
s1 = set(t1)
print(s1)
print(type(s1))

# converting tuple to string(str)
v = str(t1)
print(v)
print(len(v))
print(type(v))

